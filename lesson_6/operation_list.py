# Длина списка
fruits = ["яблоко", "банан", "вишня"]
print(len(fruits))  # 3
print()

# Проверка наличия элемента
fruits = ["яблоко", "банан", "вишня"]
print("яблоко" in fruits)  # True
print("груша" in fruits)   # False
print()

# Конкатенация списков
list1 = [1, 2, 3]
list2 = [4, 5, 6]
combined = list1 + list2
print(combined)  # [1, 2, 3, 4, 5, 6]
print()

# Повторение списка
numbers = [0] * 5
print(numbers)  # [0, 0, 0, 0, 0]