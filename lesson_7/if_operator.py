# Использование IF
age = 18
if age >= 18:
    print("Вы совершеннолетний.")
print()

# Использование IF-ELSE
age = 16
if age >= 18:
    print("Вы совершеннолетний.")
else:
    print("Вы несовершеннолетний.")
print()

# Использование IF-ELIF-ELSE
score = 85
if score >= 90:
    print("Отлично!")
elif score >= 70:
    print("Хорошо.")
else:
    print("Нужно постараться.")